class trie
{
private:
	class trie_node
	{
	private:
		bool terminal = false;
		trie_node* next[26] = { 0 };

	public:
		int convert(const char* s, int& p)
		{
			if (s[p] == 'a')
			{
				terminal = true;
				p++;
			}

			while (s[p] < 'a')
			{
				int ni = s[p++] - 'A';
				next[ni] = new trie_node();
				int back = next[ni]->convert(s, p);
				if (back > 1)
					return back - 1;
			}

			terminal = true;
			return s[p++] - 'a';
		}

		int function(const char input[25], int p, int bitmask)
		{
			int r = 0;

			char cc = input[p] - 'A';
			if (next[cc] == 0)
				return 0;

			r += next[cc]->terminal;
			next[cc]->terminal = false;
			int nis[] = { p - 6, p - 5, p - 4, p - 1, p + 1, p + 4, p + 5, p + 6 };

			for (int i = 0; i < sizeof_array(nis); i++)
			{
				int ni = nis[i];

				if (ni < 0 || ni >= 25)
					continue;
				if (abs(p % 5 - ni % 5) > 1)
					continue;
				if (bitmask & (1 << ni))
					continue;

				r += next[cc]->function(input, ni, bitmask | (1 << ni));
			}

			return r;

		}
	};
	trie_node* front;

public:
	trie()
	{
		front = new trie_node();
	}

	void convert(const char* string)
	{
		int p = 0;
		front->convert(string, p);
	}

	int function(const char input[25])
	{
		int result = 0;
		for (int i = 0; i < 25; i++)
			result += front->function(input, i, 1 << i);

		return result;
	}
};

